#include <ctype.h>
#include <stdio.h>
#include <unistd.h>

int	ft_isalnum(int n)
{
	if ((n >= 65 && n <= 90) || (n >= 97 && n <= 122) || (n >= 48 && n <= 57))
		return (1);
	return (0);
}
